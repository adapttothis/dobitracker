let currentLang = localStorage.getItem('selectedLang') || 'en'; // language switching functionality
let balance = parseInt(localStorage.getItem('balance')) || 0;
let historyLog = JSON.parse(localStorage.getItem('historyLog')) || [];
let alertEnabled = localStorage.getItem('alertEnabled') !== 'false'; // defaults to true

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('selectedLang', lang);
    updateUI();
}

function updateUI() {
    const t = translations[currentLang];
    document.getElementById('text-current-balance').textContent = t.currentBalance;
    document.getElementById('btn-wash').textContent = t.wash;
    document.getElementById('btn-dry').textContent = t.dry;
    document.getElementById('btn-topup').textContent = t.reload;
    document.getElementById('text-recent-activity').textContent = t.recentActivity;
    document.getElementById('btn-undo').textContent = t.undo;
    document.getElementById('text-disclaimer').textContent = t.disclaimer;
    document.getElementById('guide-title').textContent = t.guideTitle;
    document.getElementById('guide-desc').textContent = t.guideDesc;
    document.getElementById('guide-how-title').textContent = t.guideHow;
    document.getElementById('text-clear-history').textContent = t.clearHistory;
    document.getElementById('low-balance-warning').textContent = t.lowBalanceWarning;
    document.getElementById('text-toggle-alert').textContent = t.toggleAlert;
    
    const stepsList = document.getElementById('guide-steps-list');
    stepsList.innerHTML = '';
    t.guideSteps.forEach(step => {
        const li = document.createElement('li');
        li.innerHTML = step;
        stepsList.appendChild(li);
    });
    document.getElementById('guide-note').innerHTML = t.guideNote;
    updatehistoryLogDisplay();
    document.getElementById('text-feedback').textContent = "💬 " + t.feedback;
}

function updatehistoryLogDisplay() {
    const historyListElement = document.getElementById('history-list');
    const t = translations[currentLang];
    
    if (historyLog.length === 0) {
        historyListElement.innerHTML = `<li class="history-item" style="color: #9ca3af;">${t.noActivity}</li>`;
    } else {
        historyListElement.innerHTML = historyLog.map(item => {
            let typeText;
            if (item.type === 'wash') typeText = t.historyLogWash;
            else if (item.type === 'dry') typeText = t.historyLogDry;
            else if (item.type === 'reload') typeText = t.historyLogReload;

            const amountClass = item.amount < 0 ? 'amount-negative' : 'amount-positive';
            const sign = item.amount > 0 ? '+' : '';

            return `
                <li class="history-item">
                    <div class="history-info">
                        <strong>${typeText}</strong>
                        <div class="history-date">${item.timestamp}</div>
                    </div>
                    <div class="history-amount ${amountClass}">${sign}RM ${Math.abs(item.amount)}</div>
                </li>
            `;
        }).join('');
    }
}

document.getElementById('lang-select').value = currentLang;
document.getElementById('lang-select').addEventListener('change', (e) => {
    setLanguage(e.target.value);
});

function updateBalanceDisplay() {
    const balanceDisplay = document.getElementById('balance-display');
    const warningText = document.getElementById('low-balance-warning');
    
    const currentBalance = Number(balance);
    balanceDisplay.textContent = currentBalance;
    localStorage.setItem('balance', currentBalance);
    
    if (currentBalance < 4 && alertEnabled === true) {
        warningText.classList.remove('hidden');
    } else {
        warningText.classList.add('hidden');
    }
}

function addTohistoryLog(type, amount) {
    const now = new Date();
    const timestamp = now.toLocaleString();
    historyLog.unshift({ type, amount, timestamp });
    if (historyLog.length > 20) historyLog = historyLog.slice(0, 20);
    localStorage.setItem('historyLog', JSON.stringify(historyLog));
    updatehistoryLogDisplay();
}

function updateBalance(amount) {
    balance += amount;
    updateBalanceDisplay();
}

document.getElementById('btn-wash').addEventListener('click', () => {
    if (balance >= 4) {
        updateBalance(-4);
        addTohistoryLog('wash', -4);
        if (typeof gtag === 'function') gtag('event', 'wash_click', { 'value': 4 });
    }
});

document.getElementById('btn-dry').addEventListener('click', () => {
    if (balance >= 5) {
        updateBalance(-5);
        addTohistoryLog('dry', -5);
        if (typeof gtag === 'function') gtag('event', 'dry_click', { 'value': 5 });
    }
});

document.getElementById('btn-topup').addEventListener('click', () => {
    const input = document.getElementById('topup-input');
    const amount = parseInt(input.value);
    if (amount > 0) {
        updateBalance(amount);
        addTohistoryLog('reload', amount);
        if (typeof gtag === 'function') gtag('event', 'reload_click', { 'value': amount });
        input.value = '';
    }
});

document.getElementById('btn-undo').addEventListener('click', () => {
    if (historyLog.length > 0) {
        const last = historyLog.shift();
        updateBalance(-last.amount);
        localStorage.setItem('historyLog', JSON.stringify(historyLog));
        updatehistoryLogDisplay();
    }
});

document.getElementById('clear-history-btn').addEventListener('click', () => {
    const t = translations[currentLang];
    
    const isConfirmed = confirm(t.confirmClear);
    
    if (isConfirmed) {
        historyLog = []; 
        localStorage.setItem('historyLog', JSON.stringify(historyLog)); 
        updatehistoryLogDisplay(); 
    }
});

document.getElementById('alert-toggle').checked = alertEnabled;

document.getElementById('alert-toggle').addEventListener('change', (e) => {
    alertEnabled = e.target.checked; 
    localStorage.setItem('alertEnabled', alertEnabled); 
    
    updateBalanceDisplay();
});

updateBalanceDisplay();
updateUI();

// Advanced Service Worker Registration for Auto-Updates
if ('serviceWorker' in navigator) {
    navigator.serviceWorker.register('sw.js').then(registration => {
        registration.addEventListener('updatefound', () => {
            const newWorker = registration.installing;
            
            newWorker.addEventListener('statechange', () => {
                if (newWorker.state === 'installed' && navigator.serviceWorker.controller) {
                    if (confirm("New Update (v1.2) Available!\n\n• Added 'Clear All' history button\n• Added Low Balance alerts (< RM 4)\n• Added alert toggle switch\n• Improved UI and animations\n\nClick OK to refresh and update.")) {
    window.location.reload();
                    }
                }
            });
        });
    });
}

